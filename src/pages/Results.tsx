import { useEffect, useState } from "react";
import { Link, useParams } from "react-router-dom";
import { SiteHeader } from "@/components/SiteHeader";
import { Button } from "@/components/ui/button";
import { Progress } from "@/components/ui/progress";
import { Badge } from "@/components/ui/badge";
import { supabase } from "@/integrations/supabase/client";
import { useAuth } from "@/hooks/useAuth";
import { AlertTriangle, ArrowLeft, CheckCircle2, Download, Lightbulb, Loader2, Sparkles } from "lucide-react";

function downloadReport(data: Analysis) {
  const esc = (s: string) =>
    s.replace(/[&<>"']/g, (c) => ({ "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;", "'": "&#39;" }[c]!));
  const list = (arr: string[]) =>
    arr.length ? `<ul>${arr.map((x) => `<li>${esc(x)}</li>`).join("")}</ul>` : "<p><em>None</em></p>";
  const chips = (arr: string[], cls: string) =>
    arr.length
      ? `<div class="chips">${arr.map((x) => `<span class="chip ${cls}">${esc(x)}</span>`).join("")}</div>`
      : "<p><em>None</em></p>";
  const scoreColor = data.score >= 80 ? "#16a34a" : data.score >= 60 ? "#d97706" : "#dc2626";
  const html = `<!doctype html><html><head><meta charset="utf-8"/>
<title>Skill-Hire ATS Report — ${esc(data.file_name)}</title>
<style>
  *{box-sizing:border-box} body{font-family:-apple-system,BlinkMacSystemFont,'Segoe UI',Roboto,sans-serif;color:#0f172a;max-width:820px;margin:32px auto;padding:0 24px;line-height:1.55}
  header{display:flex;justify-content:space-between;align-items:flex-start;border-bottom:2px solid #6366f1;padding-bottom:16px;margin-bottom:24px}
  h1{margin:0;font-size:26px} h2{font-size:18px;margin:28px 0 10px;border-left:4px solid #6366f1;padding-left:10px}
  .score{font-size:56px;font-weight:800;color:${scoreColor};line-height:1}
  .meta{color:#64748b;font-size:13px}
  ul{margin:6px 0 0 18px;padding:0} li{margin:4px 0}
  .chips{display:flex;flex-wrap:wrap;gap:6px;margin-top:6px}
  .chip{padding:4px 10px;border-radius:999px;font-size:12px;border:1px solid #e2e8f0}
  .chip.found{background:#dcfce7;border-color:#bbf7d0;color:#166534}
  .chip.missing{background:#fee2e2;border-color:#fecaca;color:#991b1b}
  .grid{display:grid;grid-template-columns:1fr 1fr;gap:20px}
  .summary{background:#f8fafc;padding:14px 16px;border-radius:8px;border:1px solid #e2e8f0}
  ol{padding-left:20px} ol li{margin:6px 0}
  footer{margin-top:36px;padding-top:14px;border-top:1px solid #e2e8f0;color:#94a3b8;font-size:12px;text-align:center}
  @media print{body{margin:0}}
</style></head><body>
<header>
  <div>
    <h1>Skill-Hire — ATS Report</h1>
    <div class="meta"><strong>${esc(data.file_name)}</strong> · ${new Date(data.created_at).toLocaleString()}</div>
  </div>
  <div style="text-align:right"><div class="meta">ATS Score</div><div class="score">${data.score}<span style="font-size:18px;color:#94a3b8">/100</span></div></div>
</header>
<div class="summary"><strong>Summary.</strong> ${esc(data.summary ?? "—")}</div>
<h2>Action plan — Suggestions to improve</h2>
<ol>${data.suggestions.map((s) => `<li>${esc(s)}</li>`).join("") || "<li><em>None</em></li>"}</ol>
<div class="grid">
  <div><h2>Strengths</h2>${list(data.strengths)}</div>
  <div><h2>Weaknesses</h2>${list(data.weaknesses)}</div>
</div>
<h2>Keywords found</h2>${chips(data.keywords_found, "found")}
<h2>Missing keywords</h2>${chips(data.keywords_missing, "missing")}
<footer>Generated by Skill-Hire · Use your browser's “Save as PDF” to export.</footer>
<script>window.addEventListener('load',()=>setTimeout(()=>window.print(),300));</script>
</body></html>`;
  const blob = new Blob([html], { type: "text/html" });
  const url = URL.createObjectURL(blob);
  // Open in new tab and auto-trigger print dialog (user can Save as PDF or Cancel to keep HTML)
  const w = window.open(url, "_blank");
  // Fallback: also offer direct download
  const a = document.createElement("a");
  a.href = url;
  a.download = `Skill-Hire-Report-${data.file_name.replace(/\.[^.]+$/, "")}.html`;
  if (!w) a.click();
  setTimeout(() => URL.revokeObjectURL(url), 60_000);
}

type Analysis = {
  id: string;
  file_name: string;
  score: number;
  summary: string | null;
  strengths: string[];
  weaknesses: string[];
  suggestions: string[];
  keywords_found: string[];
  keywords_missing: string[];
  created_at: string;
};

export default function Results() {
  const { id } = useParams();
  const { user, loading: authLoading } = useAuth();
  const [data, setData] = useState<Analysis | null>(null);
  const [loading, setLoading] = useState(true);
  const [err, setErr] = useState<string | null>(null);

  useEffect(() => {
    if (!id || authLoading) return;
    if (!user) {
      setErr("Please sign in.");
      setLoading(false);
      return;
    }
    (async () => {
      const { data: row, error } = await supabase
        .from("resume_analyses")
        .select("*")
        .eq("id", id)
        .maybeSingle();
      if (error) setErr(error.message);
      else if (!row) setErr("Analysis not found.");
      else setData(row as unknown as Analysis);
      setLoading(false);
    })();
  }, [id, user, authLoading]);

  if (loading || authLoading) {
    return (
      <div className="flex min-h-screen items-center justify-center">
        <Loader2 className="h-6 w-6 animate-spin text-primary" />
      </div>
    );
  }

  if (err || !data) {
    return (
      <div className="min-h-screen bg-gradient-soft">
        <SiteHeader />
        <div className="container py-20 text-center">
          <p className="text-muted-foreground">{err ?? "Not found."}</p>
          <Link to="/dashboard" className="mt-4 inline-block">
            <Button variant="outline">Back to dashboard</Button>
          </Link>
        </div>
      </div>
    );
  }

  const scoreColor =
    data.score >= 80
      ? "text-success"
      : data.score >= 60
      ? "text-warning"
      : "text-destructive";

  return (
    <div className="min-h-screen bg-gradient-soft">
      <SiteHeader />
      <div className="container max-w-5xl py-12">
        <Link to="/dashboard" className="mb-6 inline-flex items-center gap-1.5 text-sm text-muted-foreground hover:text-foreground">
          <ArrowLeft className="h-4 w-4" /> All reports
        </Link>

        {/* Score card */}
        <div className="mb-8 grid gap-6 md:grid-cols-[1fr_2fr]">
          <div className="rounded-3xl border border-border/60 bg-card p-8 text-center shadow-card">
            <p className="text-xs font-medium uppercase tracking-wider text-muted-foreground">ATS Score</p>
            <p className={`mt-3 text-7xl font-bold tabular-nums ${scoreColor}`}>{data.score}</p>
            <p className="text-sm text-muted-foreground">out of 100</p>
            <Progress value={data.score} className="mt-5 h-2" />
          </div>
          <div className="rounded-3xl border border-border/60 bg-card p-8 shadow-card">
            <div className="mb-3 flex items-center gap-2">
              <Sparkles className="h-4 w-4 text-primary" />
              <span className="text-xs font-medium uppercase tracking-wider text-muted-foreground">
                AI summary · {data.file_name}
              </span>
            </div>
            <p className="text-base leading-relaxed">{data.summary ?? "—"}</p>
          </div>
        </div>

        {/* Suggestions (most actionable) */}
        <Section
          icon={<Lightbulb className="h-5 w-5" />}
          title="Suggestions to improve"
          accent="primary"
        >
          <ol className="space-y-3">
            {data.suggestions.map((s, i) => (
              <li key={i} className="flex gap-3 rounded-xl bg-secondary/60 p-4">
                <span className="flex h-7 w-7 shrink-0 items-center justify-center rounded-full bg-gradient-hero text-sm font-semibold text-primary-foreground">
                  {i + 1}
                </span>
                <span className="text-sm leading-relaxed">{s}</span>
              </li>
            ))}
          </ol>
        </Section>

        {/* Strengths + weaknesses */}
        <div className="mt-6 grid gap-6 md:grid-cols-2">
          <Section
            icon={<CheckCircle2 className="h-5 w-5" />}
            title="Strengths"
            accent="success"
          >
            <ul className="space-y-2">
              {data.strengths.map((s, i) => (
                <li key={i} className="flex gap-2 text-sm">
                  <CheckCircle2 className="mt-0.5 h-4 w-4 shrink-0 text-success" />
                  <span>{s}</span>
                </li>
              ))}
            </ul>
          </Section>
          <Section
            icon={<AlertTriangle className="h-5 w-5" />}
            title="Weaknesses"
            accent="destructive"
          >
            <ul className="space-y-2">
              {data.weaknesses.map((s, i) => (
                <li key={i} className="flex gap-2 text-sm">
                  <AlertTriangle className="mt-0.5 h-4 w-4 shrink-0 text-destructive" />
                  <span>{s}</span>
                </li>
              ))}
            </ul>
          </Section>
        </div>

        {/* Keywords */}
        <div className="mt-6 grid gap-6 md:grid-cols-2">
          <div className="rounded-3xl border border-border/60 bg-card p-6 shadow-card">
            <h3 className="mb-3 text-sm font-semibold uppercase tracking-wider text-muted-foreground">
              Keywords found
            </h3>
            <div className="flex flex-wrap gap-2">
              {data.keywords_found.length ? (
                data.keywords_found.map((k) => (
                  <Badge key={k} variant="secondary" className="bg-success/10 text-success hover:bg-success/15">
                    {k}
                  </Badge>
                ))
              ) : (
                <span className="text-sm text-muted-foreground">None detected.</span>
              )}
            </div>
          </div>
          <div className="rounded-3xl border border-border/60 bg-card p-6 shadow-card">
            <h3 className="mb-3 text-sm font-semibold uppercase tracking-wider text-muted-foreground">
              Keywords missing
            </h3>
            <div className="flex flex-wrap gap-2">
              {data.keywords_missing.length ? (
                data.keywords_missing.map((k) => (
                  <Badge key={k} variant="outline" className="border-destructive/30 text-destructive">
                    {k}
                  </Badge>
                ))
              ) : (
                <span className="text-sm text-muted-foreground">Looks complete.</span>
              )}
            </div>
          </div>
        </div>

        <div className="mt-10 flex flex-col items-center justify-center gap-3 sm:flex-row">
          <Button
            size="lg"
            variant="outline"
            onClick={() => downloadReport(data)}
          >
            <Download className="mr-2 h-4 w-4" /> Download PDF report
          </Button>
          <Link to="/upload">
            <Button size="lg" className="bg-gradient-hero text-primary-foreground shadow-elegant hover:opacity-95">
              Analyze another resume
            </Button>
          </Link>
        </div>
      </div>
    </div>
  );
}

function Section({
  icon,
  title,
  accent,
  children,
}: {
  icon: React.ReactNode;
  title: string;
  accent: "primary" | "success" | "destructive";
  children: React.ReactNode;
}) {
  const accentClass =
    accent === "success"
      ? "text-success"
      : accent === "destructive"
      ? "text-destructive"
      : "text-primary";
  return (
    <div className="rounded-3xl border border-border/60 bg-card p-6 shadow-card">
      <div className={`mb-4 flex items-center gap-2 ${accentClass}`}>
        {icon}
        <h2 className="text-lg font-semibold text-foreground">{title}</h2>
      </div>
      {children}
    </div>
  );
}
